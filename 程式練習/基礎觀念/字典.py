# =============================================================================
# 字典 是 Python 中一種非常常用的資料結構，它用來儲存一組 鍵-值對 (key-value pairs)。
# 你可以想像它就像一本真正的字典，每個單字 (鍵) 對應著一個解釋 (值)。
# 
# 鍵 必須是唯一的，不可變的資料型態，例如字串、數字或元組。
# 而 值 可以是任何資料型態，甚至可以是另一個字典。
# =============================================================================



































